#include <bits/stdc++.h>
using namespace std;

// 타입 상관없이 2d array를 출력함
template <typename T>
void printArr(vector<vector<T>> &arr) {
  for (int i = 0; i < arr.size(); i++) {
    for (int j = 0; j < arr.size(); j++) {
      cout << arr[i][j] << " ";
    }
    cout << "\n";
  }

  cout << "\n";
};

int main() { return 0; }